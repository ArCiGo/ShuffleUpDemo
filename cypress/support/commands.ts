/**
 * My idea was to move out the logic of navigation outside of the LoginPage class,
 * so it wouldn't be just part of that class but of all the other classes. This way
 * the user can navigate to any page if he/she wishes by passing the desired URL.
 */
Cypress.Commands.add('navigateTo', (url: string) => {
    cy.visit(url);
});